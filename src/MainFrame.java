    private void scheduleSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleSubmitButtonActionPerformed
        // TODO add your handling code here:
        String semester = currentSemester;
        String courseCode = studentClassComboBox.getSelectedItem().toString();
        String status;
        String studentID = ((StudentEntry) studentNameComboBox.getSelectedItem()).getStudentID();
        int classSeats = ClassQueries.getClassSeats(semester, courseCode);
        int scheduled = ScheduleQueries.getScheduledStudentCount(semester, courseCode);
        String text;

        if (classSeats > scheduled){
            status = "Scheduled";
            text = "scheduled";
        }
        else{
            status = "Waitlisted";
            text = "waitlisted";
        }
        ScheduleEntry entry = new ScheduleEntry(semester,courseCode,studentID,status);
        ScheduleQueries.addScheduleEntry(entry);
        statusLabel.setText((StudentEntry) studentNameComboBox.getSelectedItem() + " has been " + text + " for the class" );
    }//GEN-LAST:event_scheduleSubmitButtonActionPerformed

