    private void studentDropClassSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentDropClassSubmitButtonActionPerformed
        // TODO add your handling code here: 
        StudentEntry student = (StudentEntry) studentDropClassComboBox.getSelectedItem();
        String studentID = student.getStudentID();
        ScheduleEntry classes = (ScheduleEntry) studentChooseClassDropComboBox.getSelectedItem();       
        String courseCode = classes.getCourseCode();
        
        rebuildstudentChooseClassDropComboBox(studentID);
        
        ScheduleQueries.dropStudentScheduleByCourse(currentSemester, student.getStudentID(), classes.getCourseCode());
        
        studentDropClassTextArea.append(student.toString() + " has been dropped from " + courseCode + "\n");
        
        ArrayList<ScheduleEntry> array = ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, courseCode);
       
        if (!array.isEmpty()){
            
            ScheduleEntry schedule = array.get(0);
            String scheduledStudent = StudentQueries.getStudent(schedule.getStudentID()).toString();
            ScheduleQueries.updateScheduleEntry(schedule);
            studentDropClassTextArea.append(scheduledStudent + " has been scheduled into " + courseCode + "\n");

        }        
    }//GEN-LAST:event_studentDropClassSubmitButtonActionPerformed

