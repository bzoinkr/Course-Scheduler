    private void dropStudentSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentSubmitActionPerformed
        // TODO add your handling code here:
        StudentEntry student = (StudentEntry) dropStudentComboBox.getSelectedItem();
        StudentQueries.dropStudent(student.getStudentID());       
        dropStudentTextArea.append(student + " has been dropped from the list of students.\n"+"\n");
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        
        for (String string:semesters){
            
            ArrayList<ScheduleEntry> dropStu = ScheduleQueries.getScheduleByStudent(string,student.getStudentID());
            dropStudentTextArea.append("For Semeser: " + string + "\n");

            for (ScheduleEntry entry:dropStu){
            
                ArrayList<ScheduleEntry> wait = ScheduleQueries.getWaitlistedStudentsByClass(string,entry.getCourseCode());
                ScheduleQueries.dropStudentScheduleByCourse(string, student.getStudentID(), entry.getCourseCode());

                if (!wait.isEmpty())
                {        
                    ScheduleEntry schedule = wait.get(0);
                    ScheduleQueries.updateScheduleEntry(schedule);
                    dropStudentTextArea.append(StudentQueries.getStudent(schedule.getStudentID()) + " has been scheduled into " + entry.getCourseCode() + "\n");
                }
            }
            dropStudentTextArea.append("\n");
        }
        rebuildClassComboBoxes();
        rebuildStudentNameComboBoxes();
        rebuilddropStudentComboBox();
        rebuilddisplayScheduleStudentComboBox();
        rebuildstudentDropClassComboBox();
        
    }//GEN-LAST:event_dropStudentSubmitActionPerformed

